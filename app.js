let web3;
let account;
let contract;

window.onload = async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.enable();
        const accounts = await web3.eth.getAccounts();
        account = accounts[0];
        document.getElementById('account').innerText = `Account: ${account}`;
        contract = new web3.eth.Contract(contractABI, contractAddress);
        updateBalance();
    } else {
        alert("Please install MetaMask!");
    }
};

async function updateBalance() {
    const balance = await contract.methods.balanceOf(account).call();
    document.getElementById('balance').innerText = `Balance: ${web3.utils.fromWei(balance, 'ether')} GC`;
}

async function registerUser() {
    const username = document.getElementById('username').value;
    const email = document.getElementById('email').value;
    await contract.methods.registerUser(username, email).send({ from: account });
}

async function buyGC() {
    const amount = document.getElementById('buyAmount').value;
    await contract.methods.buy(web3.utils.toWei(amount, 'ether')).send({ from: account});
    updateBalance();
}

async function sellGC() {
    const amount = document.getElementById('sellAmount').value;
    await contract.methods.sell(web3.utils.toWei(amount, 'ether')).send({ from: account });
    updateBalance();
}

async function sendGC() {
    const recipient = document.getElementById('recipient').value;
    const amount = document.getElementById('sendAmount').value;
    await contract.methods.send(recipient, web3.utils.toWei(amount, 'ether')).send({ from: account });
    updateBalance();
}

